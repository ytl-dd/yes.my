            if (window.FileReader && window.File && window.FileList && window.Blob) {
                if ($("#uploadfile")[0].files[0].size > 1048576) {
                    $fileErrors.filter("[type=exceed]").show();
                    valid = false;
                }
            }
        }

        return valid;
    }
