    function validateForm() {
        var valid = true;

        var email_regex = /^([A-Za-z0-9_\-\.])+\@([A-Za-z0-9_\-\.])+\.([A-Za-z]{2,4})$/;
        var file_regex = /^([\w\W\s])+\.(pdf|jpg|png|doc|docx|ppt|pptx)$/;

        function validateInput($input) {
            var $errors = $input.parents(".form-group").find(".error-msg > li").hide();
            var value = $input.val();

            var valid = true;

            if (value == '') {
                $errors.filter("[type=empty]").show();

                valid = false;
            }

            if ($input.attr("data-validate")) {
                var validate = $input.attr("data-validate").split(" ");

                for (var i = 0; i < validate.length; i++) {
                    if (validate == "email") {
                        if (!email_regex.test(value)) {
                            $errors.filter("[type=email]").show();

                            valid = false;
                        }
                    }
                }
            }

            return valid;
        }

        $("input[required]").each(function() {
            var rs = validateInput($(this));

            if (!rs) valid = rs;
        });

        var qAmt = 0;
        var eAmt = 0;

        $(".row-q:first").each(function() {
            var qValid = true;

            $("input[name]", this).each(function() {
                var rs = validateInput($(this));
                if (!rs) qValid = rs;
            });

            if (qValid) {
                qAmt++;
            }
        });

        $(".row-e:first").each(function() {
            var eValid = true;

            $("input[name]", this).each(function() {
                var rs = validateInput($(this));
                if (!rs) eValid = rs;
            });

            if (eValid) {
                eAmt++;
            }
        });

        if (qAmt == 0) valid = false;
        if (eAmt == 0) valid = false;

        var fileName = $('#fileName').val();
        var $fileErrors = $("#fileErrorMsgs > li").hide();

        if (!file_regex.test(fileName)) {
            $fileErrors.filter("[type=empty]").show();

            valid = false;
